    //Crie um programa que verifica se um número é positivo e imprime mensagens dentro de blocos:


public class blocos {
  public static void main(String[] args) {
        int numero = -5;

        if (numero > 0) {
            System.out.println("Número positivo");

            if (numero > 10) {
                System.out.println("Número maior que 10");
            }
        } else {
            System.out.println("Número negativo ou zero");
        }
    }
}
    

